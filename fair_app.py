    "    # Encode categorical variables\n",
    "    df[\"VACATION\"] = df[\"VACATION\"].map({\"Yes\": 1, \"No\": 0})\n",
    "    df[\"SW\"] = df[\"SW\"].map({\"Yes\": 1, \"No\": 0})\n",
    "    df[\"SLOT\"] = df[\"SLOT\"].map({\"Controlled\": 1, \"Free\": 0})\n",
    "    df[\"GATE\"] = df[\"GATE\"].map({\"Controlled\": 1, \"Free\": 0})\n",
    "\n",
    "    # Define X and y\n",
    "    X = df[[\"COUPON\", \"NEW\", \"VACATION\", \"SW\", \"HI\", \"S_INCOME\",\n",
    "             \"E_INCOME\", \"S_POP\", \"E_POP\", \"SLOT\", \"GATE\", \"PAX\", \"DISTANCE\"]]\n",
    "    y = df[\"FARE\"]\n",
    "\n",
    "    X = X.replace([np.inf, -np.inf], np.nan).dropna()\n",
    "    y = y.loc[X.index]\n",
    "\n",
    "    X = sm.add_constant(X)\n",
    "    model = sm.OLS(y, X).fit()\n",
    "\n",
    "    # Add predictions and residuals\n",
    "    df[\"Predicted_FARE\"] = model.predict(sm.add_constant(X))\n",
    "    df[\"Residuals\"] = df[\"FARE\"] - df[\"Predicted_FARE\"]\n",
    "\n",
    "    return df, model\n",
